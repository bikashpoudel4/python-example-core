"""Ternary Operator"""

# example 1
x = 3
answer = "positive" if x > 0 else "negative"
print("1.X is greater than 3 then answer should be positive else negative -----", answer)

# example 2
a = -3
answer2 = "positive" if a > 0 else "negative"
print("2.a is less than -3 so answer is negative ---------", answer2)

# example 3 
